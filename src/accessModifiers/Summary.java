package accessModifiers;

public class Summary {  // parent class
    /*
    Modifiers -> 1. Non Access Modifiers  ( static and final)
                 2. Access Modifiers

              a) public: it is accessible from everwhere
              b) private: it is accessible ONLY in the same class
              c)default: it is accessible in the same class and same package
              d)protected: accessible in the same class, same package, different package but for the subclass
     */

    public int age=69;
    protected double num=3.5;
    String name="Tuba";
    private boolean isFriday=false;

}
