package whileDoWhile;

public class WhileLoopTask {
    public static void main(String[] args) {
         /*

        create one char which is a

        print all the char from a - z

        in the while loop

        Create the alphabet

         */
         char c='a';
         while(c<='z'){
             System.out.print(c+" ");
             c++;
         }
    }
}
